package com.twinspires.qa.core.testobjects;

public class DatastoreRegistrationInfo {

    String camId;
    String gateway;
    String marketingAffiliate;
    String firstName;
    String lastName;
    String emailAddress;
    String promotionCode;
    String status;

    public String getCamId() { return camId; }

    public void setCamId(String camId) { this.camId = camId; }

    public String getGateway() { return gateway; }

    public void setGateway(String gateway) { this.gateway = gateway; }

    public String getMarketingAffiliate() { return marketingAffiliate; }

    public void setMarketingAffiliate(String marketingAffiliate) { this.marketingAffiliate = marketingAffiliate; }

    public String getFirstName() { return firstName; }

    public void setFirstName(String firstName) { this.firstName = firstName; }

    public String getLastName() { return lastName; }

    public void setLastName(String lastName) { this.lastName = lastName; }

    public String getEmailAddress() { return emailAddress; }

    public void setEmailAddress(String emailAddress) { this.emailAddress = emailAddress; }

    public String getPromotionCode() { return promotionCode; }

    public void setPromotionCode(String promotionCode) { this.promotionCode = promotionCode; }

    public String getStatus() { return status; }

    public void setStatus(String status) { this.status = status; }

}
