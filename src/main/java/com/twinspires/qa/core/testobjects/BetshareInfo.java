package com.twinspires.qa.core.testobjects;

public class BetshareInfo {

    String ticket;
    String trackCode;
    String race;
    String poolTypeId;
    String runners;
    String amount;
    String cost;

    public String getTicket() {
        return ticket;
    }

    public void setTicket(String ticket) {
        this.ticket = ticket;
    }

    public String getTrackCode() {
        return trackCode;
    }

    public void setTrackCode(String trackCode) {
        this.trackCode = trackCode;
    }

    public String getRace() {
        return race;
    }

    public void setRace(String race) {
        this.race = race;
    }

    public String getPoolTypeId() {
        return poolTypeId;
    }

    public void setPoolTypeId(String poolTypeId) {
        this.poolTypeId = poolTypeId;
    }

    public String getRunners() {
        return runners;
    }

    public void setRunners(String runners) {
        this.runners = runners;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

}
